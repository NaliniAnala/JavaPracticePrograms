package javaPractiesPrograms;

public class While4 {

	public static void main(String[] args) {
		/*
		 * Write a java program to print "Hello AccentechPartners how are you" three
		 * times using the while loop
		 * Step 1: Initialize the value int i=1
		 * Step 2: Write the while loop conditions and print the output
		 * "Hello Accentech Partners how are you" three times
		 */
		
		int i=1;
		while(i<=3)
		{
			System.out.println("\"Hello Accentech Partners how are you\"");
			i++;
		}
		
	}

}
